# JY: 实体识别配置 - 从extracted_text的JSON格式中识别实体
# 常用实体正则配置（按需扩展/调整）
# 注意：正则表达式按优先级排列，越具体的模式应该越靠前
ENTITY_PATTERNS = {
    # 身份证：15位或18位（最后一位可以是X）
    "身份证": r"(?:^|\s)\d{15}(?:\d{2}[\dXx])?(?:\s|$)",
    
    # 联系电话：手机号或座机号
    "联系电话": r"1[3-9]\d{9}|0\d{2,4}-\d{7,8}",
    
    # 项目编号：字母+数字组合，通常包含年份和序号
    "项目编号": r"[A-Z]{1,10}\d{4}[-_]?\d{3,4}",
    
    # 公司名称：以"公司"、"集团"、"有限公司"等关键词结尾
    # 为避免误识别，要求前面有2-30个字符（汉字、英文、数字等）
    "公司": r"[\u4e00-\u9fa5A-Za-z0-9·\-]{2,30}(?:公司|集团|有限责任公司|股份有限公司|有限公司|企业|法人)",
    
    # 人名：2-4个汉字（因为中文人名识别很复杂）
    # 通常出现在"负责人"、"联系人"、"法定代表人"等标签之后，使用后向断言只匹配名字部分
    "人名": r"(?:负责人|联系人|法定代表人|姓名)(?:：|:)?([\u4e00-\u9fa5]{2,4})",
}

# JY: 实体识别优先级顺序 - 具体的模式优先识别
DEFAULT_ENTITIES = ["身份证", "联系电话", "项目编号", "公司", "人名"]